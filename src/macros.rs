/// Borrowed from the gtk-rs docs and tutorial
#[macro_export]
macro_rules! mclone {
    (@param _) => ( _ );
    (@param $x:ident) => ( $x );
    ($($n:ident),+ => move || $body:expr) => (
        {
            $( let $n = $n.clone(); )+
            move || $body
        }
    );
    ($($n:ident),+ => move |$($p:tt),+| $body:expr) => (
        {
            $( let $n = $n.clone(); )+
            move |$(mclone!(@param $p),)+| $body
        }
    );
}
